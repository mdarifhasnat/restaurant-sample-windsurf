export const getMenuItemsByCategory = (categoryId: string): MenuItem[] => {
  return menuItems.filter(item => item.categoryId === categoryId);
};

export const getMenuItemById = (id: string): MenuItem | undefined => {
  return menuItems.find(item => item.id === id);
};

export const getFeaturedItems = (): MenuItem[] => {
  return menuItems.filter(item => item.featured && item.available);
};

export const getAvailableItems = (): MenuItem[] => {
  return menuItems.filter(item => item.available);
};

export const searchMenuItems = (query: string): MenuItem[] => {
  const lowerQuery = query.toLowerCase();
  return menuItems.filter(
    item =>
      item.name.toLowerCase().includes(lowerQuery) ||
      item.nameEn.toLowerCase().includes(lowerQuery) ||
      item.description.toLowerCase().includes(lowerQuery) ||
      item.descriptionEn.toLowerCase().includes(lowerQuery) ||
      item.ingredients.some(ing => ing.toLowerCase().includes(lowerQuery))
  );
};
